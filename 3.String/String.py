# String litera o escapar caracteres
cadena = "Esto es un \"ejemplo\" de cadena de texto"
print(cadena)
cadena = "Esto es un  \nejemplo\n de cadena de texto"
print(cadena)
cadena = "Esto es un  \tejemplo\t de cadena de texto"
print(cadena)
cadena = "Esto es un  \bejemplo\b de cadena de texto"
print(cadena)
cadena = "Esto es un  \rejemplo\r de cadena de texto"
print(cadena)
cadena = "Esto es un  \vejemplo\v de cadena de texto"
print(cadena)
